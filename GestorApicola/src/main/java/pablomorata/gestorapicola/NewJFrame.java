    public NewJFrame() {
        initComponents();
        modelo.addColumn("ID");
        modelo.addColumn("Abejas");
        modelo.addColumn("Marcos");
        modelo.addColumn("Miel");
        modelo.addColumn("Estado");

        String Dato[] = new String[5];
        modelo.setRowCount(1);

        try {
            //CONECTA A LA BD
            connection = this.connect();
            statement = connection.createStatement();
            statement.setQueryTimeout(20);
            //QUERY QUE JALA TODAS LAS PUBLICACIONES
            String query = "SELECT "
                    + "ID, "
                    + "Abejas, "
                    + "Marcos, "
                    + "Miel, "
                    + "Estado"
                    + "FROM Colmena;";
            ResultSet rs = statement.executeQuery(query);
            //CICLO QUE LLENA TODO EL MODELO
            while (rs.next()) {
                Dato[0] = rs.getString("ID");
                Dato[1] = rs.getString("Abejas");
                Dato[2] = rs.getString("Marcos");
                Dato[3] = rs.getString("Miel");
                Dato[4] = rs.getString("Estado");

                modelo.addRow(Dato);
            }
            //LLENAMOS LA TABLA CON EL MODELO
            ColmenasTable.setModel(modelo);
            //OCULTAMOS EL ID EN LA TABLA
            //tabla.getColumn("id").setMaxWidth(50);
        } catch (SQLException e) {
            System.err.println(e.getMessage());
        } finally {

            try {
                if (connection != null) {
                    connection.close();
                }
            } catch (SQLException e) {
                // connection close failed.
                System.err.println(e);
            }

        }
    }

